console.dir(window);              // Opens up a window displaying info about a website
console.dir(window.document);     // Brings the info about the document
console.dir(window.innerHeight);  // Finds inner height of window in pixels
console.dir(window.innerWidth);   // Finds inner width of window in pixels
console.dir(window.outerHeight);  // Finds outer height of window in pixels
console.dir(window.outerWidth);   // Finds outer width of window in pixels

// Practical exercise 2
